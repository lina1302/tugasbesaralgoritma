    private void tampilkan() {
        int row = tabel.getRowCount();
        for(int a= 0; a<row; a++){
            model.removeRow(0);
        }
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/perpustakaan","root","");
            ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM data_buku_perpustakaan");
            while(rs.next()){
                String data[]={rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DataBuku.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
