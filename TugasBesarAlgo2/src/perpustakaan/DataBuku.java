    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        try {
            // TODO add your handling code here:
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/perpustakaan","root","");
            cn.createStatement().executeUpdate("insert into data_buku_perpustakaan values"+"('"+kode_buku.getText()
                    +"','"+judul_buku.getText()+"','"+penerbit.getText()+"','"+pengarang.getText()+"','"
                    +tahun_terbit.getText()+"','"+rak_buku.getText()+"')");
            tampilkan();
            reset();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ada yang belum di isi !!!");
         
        }
    }//GEN-LAST:event_btntambahActionPerformed

